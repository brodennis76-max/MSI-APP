// Add clients to Firebase
const addClientsToFirebase = async () => {
  try {
    console.log('Starting to add clients to Firebase...');
    
    for (const client of clients) {
      // Sanitize client name for use as document ID
      const sanitizedName = client.name.replace(/[^a-zA-Z0-9]/g, '_');
      
      const docRef = doc(db, 'clients', sanitizedName);
      await setDoc(docRef, {
        name: client.name,
        preInventory: "",
        inventoryType: "scan",
        PIC: "Stores to be contacted via phone prior to counts to confirm inventory.",
        startTime: "",
        verification: "",
        sections: [
          {
            sectionName: "Pre-Inventory",
            content: "General pre-inventory instructions for the team.",
            subsections: [
              {
                sectionName: "Area Mapping",
                content: "Instructions on mapping each area of the store prior to inventory."
              },
              {
                sectionName: "Store Prep Instructions",
                content: "Instructions for preparing the store, including clearing shelves and labeling sections."
              }
            ]
          }
        ],
        createdAt: new Date(),
        updatedAt: new Date(),
        active: true
      });
      console.log(`Added client: ${client.name} with ID: ${sanitizedName}`);
    }
    
    console.log('All clients added successfully!');
  } catch (error) {
    console.error('Error adding clients:', error);
  }
};

// Run the function
addClientsToFirebase();
